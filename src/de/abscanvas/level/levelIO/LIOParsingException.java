package de.abscanvas.level.levelIO;

public class LIOParsingException extends Exception {
	private static final long serialVersionUID = 7118559813632651838L;

	public LIOParsingException() {
	}

	public LIOParsingException(String arg0) {
		super(arg0);
	}

	public LIOParsingException(Throwable arg0) {
		super(arg0);
	}

	public LIOParsingException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public LIOParsingException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
